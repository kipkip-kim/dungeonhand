function getBgKey(screen, floor, isBoss) {
  if (screen === "battle" || screen === "reward" || screen === "enhance" || screen === "relicReward") {
    return "battle-" + Math.max(1, Math.min(5, floor || 1));
  }
  if (screen === "campfire") return "campfire";
  if (screen === "shop") return "shop";
  if (screen === "map" || screen === "event" || screen === "village") return "village";
  return "menu";
}


